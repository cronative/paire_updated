class CustomTextField extends StatefulWidget {
  final String? title;
  final TextEditingController? textEditingController;
  final bool? isSecureText;
  final bool? isPasswordField;
  Function? validator;
  Function? onChange;
  TextInputType? keyboardType;
  CustomTextField(
      {Key? key,
      this.title,
      this.textEditingController,
      this.isSecureText,
      this.validator,
      this.onChange,
      this.isPasswordField})
      : super(key: key);

  @override
  State<CustomTextField> createState() => _CustomTextFieldState();
}

class _CustomTextFieldState extends State<CustomTextField> {
  bool isPasswordFiled = false;
  bool isSecure = false;
  @override
  void initState() {
    // TODO: implement initState
    super.initState();

    if (widget.isSecureText != null) {
      isSecure = widget.isSecureText!;
    }

    if (widget.isPasswordField != null) {
      isPasswordFiled = widget.isPasswordField!;
    }
  }

  @override
  Widget build(BuildContext context) {
    return Container(
        // padding: const EdgeInsets.symmetric(horizontal: 20),
        child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
      // Padding(
      //   padding: const EdgeInsets.only(bottom: 10),
      //   child: Text(
      //     widget.title!,
      //     style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 18),
      //   ),
      // ),
      SizedBox(
          // height: 60,
          child: TextFormField(
        controller: widget.textEditingController,
        validator: (value) {
          return widget.validator!(value);
        },
        obscureText: isSecure,
        style: TextStyle(
            color: Constants.greenColor,
            fontSize: Constants.ansFontSize,
            fontWeight: FontWeight.w500),
        onChanged: (val) {
          // print(val);
          if (widget.onChange != null) {
            widget.onChange!(val);
          }
        },
        decoration: InputDecoration(
          hintText: widget.title!,
          hintStyle:
              TextStyle(color: Colors.white, fontSize: Constants.ansFontSize),
          labelStyle:
              TextStyle(color: Colors.white, fontSize: Constants.ansFontSize),
          errorStyle:
              TextStyle(color: Colors.red, fontSize: Constants.ansFontSize),
          suffixIcon: isPasswordFiled
              ? IconButton(
                  onPressed: () {
                    setState(() {
                      if (isSecure) {
                        isSecure = false;
                      } else {
                        isSecure = true;
                      }
                    });
                  },
                  icon:
                      Icon(isSecure ? Icons.visibility : Icons.visibility_off))
              : null,
          focusedBorder: const UnderlineInputBorder(
            borderSide: BorderSide(color: Colors.white),
          ),
          focusedErrorBorder: const UnderlineInputBorder(
            borderSide: BorderSide(color: Colors.white),
          ),
          errorBorder: const UnderlineInputBorder(
            borderSide: BorderSide(color: Colors.white),
          ),
          enabledBorder: const UnderlineInputBorder(
            borderSide: BorderSide(color: Colors.white),
          ),
        ),
      ))
    ]));
  }
}

